# LAÇOS DE REPETIÇÃO - Estrutura de repetição while

x = int(0)

while x != 200: # executar as instruções dentro do while 200 vezes
    print(x)
    x += 1

# o mesmo o laço for seria

for c in range (0 , 200):
    print(c)


